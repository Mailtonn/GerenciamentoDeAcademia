package Model;

public class Modalidade {
	
	private int codigoDaModalidade;
	private String nomeDaModalidade; 
	private double valor;
	
	public int getCodigoDaModalidade() {
		return codigoDaModalidade;
	}
	
	public void setCodigoDaModalidade(int codigoDaModalidade) {
		this.codigoDaModalidade = codigoDaModalidade;
	}
	
	public String getNomeDaModalidade() {
		return nomeDaModalidade;
	}
	
	public void setNomeDaModalidade(String nomeDaModalidade) {
		this.nomeDaModalidade = nomeDaModalidade;
	}
	
	public double getValor() {
		return valor;
	}
	
	public void setValor(double valor) {
		this.valor = valor;
	}
	
	

}
